package com.matchadevs.styleworks.utils;

public class Stickers {

    int stickerthumbnail;
    int sticker;

    public Stickers(int stickerthumbnail,int sticker)
    {
        this.stickerthumbnail=stickerthumbnail;
        this.sticker=sticker;
    }
    public int getStickerthumbnail()
    {
        return stickerthumbnail;
    }
    public int getSticker()
    {
        return sticker;
    }

}
